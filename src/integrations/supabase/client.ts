import { createClient } from "@supabase/supabase-js";
import type { Database } from "./types";

const SUPABASE_URL = import.meta.env.VITE_SUPABASE_URL;
const SUPABASE_ANON_KEY = import.meta.env.VITE_SUPABASE_ANON_KEY;

console.log("VITE_SUPABASE_URL", SUPABASE_URL);
console.log("VITE_SUPABASE_ANON_KEY", SUPABASE_ANON_KEY?.slice(0, 8));

if (!SUPABASE_URL) {
  throw new Error("Missing VITE_SUPABASE_URL. Did you configure your .env file?");
}

if (!SUPABASE_ANON_KEY) {
  throw new Error("Missing VITE_SUPABASE_ANON_KEY. Did you configure your .env file?");
}

export const supabase = createClient<Database>(
  SUPABASE_URL,
  SUPABASE_ANON_KEY,
  {
    auth: {
      storage: localStorage,
      persistSession: true,
      autoRefreshToken: true,
    }
  }
)
